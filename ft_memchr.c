void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*src;
	size_t			i;

	i = 0;
	src = (unsigned char *)s;
	while (i < n)
	{
		if (src[i] == c)
			return ((void *)s + i * sizeof(char));
		i++;
	}
	return (NULL);
}
